# Unlike other programming languages, Python does
# not have BLOCK SCOPE. 

# If you create a variable within a function, it's
# only available within that function. But variables
# that are created within 'if/elif/else' statements
# or within 'for' and 'while' loops are not effected
# by local scope; they are available to be use even
# though they were defined & stored within the indentation
# of those non-function actions.