# GUI

# A GUI (or Graphical User Interface) is a way for the user to interact with
# the computer hardware (and the kernel) graphically. This is usually
# represented on a computer by a window that contains elements that receive
# user inputs (via the mouse & keyboard, for example).

# CLI

# The CLI (Command-Line Interface) is another type of GUI that users can
# wield to interact with their computers & is usually encountered as a
# "terminal" or "console"-type window where the user eschews the use of the
# mouse in favor of issuing commands via their keyboard.