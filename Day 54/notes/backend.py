# There are 3 components to what's commonly known as the "backend":

# 1.    The Client — the client is the user, the platform (computer & OS),
#       as well as the browser.

# 2.    The Server — the server is the remote machine that is set up to
#       receive requests from the internet (from clients).

# 3.    The Database — the database is located on the server & holds
#       information that the server retrieves to send to the client.