# Library:

# A library (or module) within Python has methods that are called upon by
# the programmer to take some action or enforce some protocol. The
# programmer "invokes" the method(s) within the library to accomplish a task.

# Framework:

# Using a framework, the programmer needs to define their own
# functions/methods and then ensure they are framework-compliant. Then,
# and only then, can the programmer invoke the framework to carry out those
# methods/functions.