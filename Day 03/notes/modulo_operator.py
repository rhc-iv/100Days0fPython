#  The Modulo (%) operator is a mathematical operator that
#  uses division to calculate a remainder depending on whether
#  the divisible is exact or not.

#  For instance, if we divide 8 by 4, the output is 2.  But
#  there is no remainder because 4 divides into 8 in 2 equal
#  parts.  We also know that if we divide 8 by 5, the output
#  is a  float, since 5 divides into 8 only once, but not 
#  equally.  The Modulo Operator allows us to calculate this
#  remainder quickly.

print("What is the modulo of 8 divided by 5?")
print(8 % 5)