# In Python, Dynamic Typing doesn't have anything to do with the act
# of typing; rather, it is meant to be a reference to data types in
# Python. Specifically, Dynamic Typing means that variable values can
# be replaced with different data types & is valid. Unlike when we
# try to perform certain operations between data types & are
# presented with TypeErrors, Dynamic Typing functions perfectly in
# Python.  For example:

#   a = 3
#   a = "Hello"
#   print(a)

# In the above example, we simply changed the data type of the
# variable "a" from an integer to a string by simply declaring it.